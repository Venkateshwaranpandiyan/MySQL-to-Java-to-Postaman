package com.example.CollegeDemo.model;

import jakarta.persistence.Entity;
import jakarta.persistence.Id;

@Entity
public class Staffs {
    @Id
    private Long id;
    private String name;
    private int age;
    private String department;

    public Staffs(){

    }

    public Staffs(Long id, String name, int age, String department) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.department = department;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }


}
